def numbers(num1, num2):
    print(num1 + num2)


def num(num1, num2):
    print(num1 - num2)  


def nums(num1, num2):
    print(num1 * num2)   


def No(num1, num2):
    print(num1 / num2)



num1 = 100
num2 = 10 

print("num:", numbers(num1, num2))
print("numbers:", num(num1, num2))
print("nums:", nums(num1, num2))
print("No", No(num1, num2))
